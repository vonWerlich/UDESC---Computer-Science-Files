package dados;

public class Departamento{

	private int numdep;
	private String nomedep;
	private String escritorioprincipal;
	private int professorlider;
	
	public int getNumdep() {
		return numdep;
	}
	public void setNumdep(int numdep) {
		this.numdep = numdep;
	}
	public String getNomedep() {
		return nomedep;
	}
	public void setNomedep(String nomedep) {
		this.nomedep = nomedep;
	}
	public String getEscritorioprincipal() {
		return escritorioprincipal;
	}
	public void setEscritorioprincipal(String escritorioprincipal) {
		this.escritorioprincipal = escritorioprincipal;
	}
	public int getProfessorlider() {
		return professorlider;
	}
	public void setProfessorlider(int professorlider) {
		this.professorlider = professorlider;
	}
	
}
