package dados;

public class Projeto {

	private int numprojeto;
	private String orgaofinanciador;
	private String datainicio;
	private String datatermino;
	private Double orcamento;
	private int pesquisadorprinc;
	
	public int getNumprojeto() {
		return numprojeto;
	}
	public void setNumprojeto(int numprojeto) {
		this.numprojeto = numprojeto;
	}
	public String getOrgaofinanciador() {
		return orgaofinanciador;
	}
	public void setOrgaofinanciador(String orgaofinanciador) {
		this.orgaofinanciador = orgaofinanciador;
	}
	public String getDatainicio() {
		return datainicio;
	}
	public void setDatainicio(String datainicio) {
		this.datainicio = datainicio;
	}
	public String getDatatermino() {
		return datatermino;
	}
	public void setDatatermino(String datatermino) {
		this.datatermino = datatermino;
	}
	public Double getOrcamento() {
		return orcamento;
	}
	public void setOrcamento(Double orcamento) {
		this.orcamento = orcamento;
	}
	public int getPesquisadorprinc() {
		return pesquisadorprinc;
	}
	public void setPesquisadorprinc(int pesquisadorprinc) {
		this.pesquisadorprinc = pesquisadorprinc;
	}

}
